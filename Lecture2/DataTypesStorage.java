package Lecture2;

public class DataTypesStorage {
	public static void main(String[] args) {
		int min = Integer.MIN_VALUE;
		int max = Integer.MAX_VALUE;
		System.out.println(min + " " + max);
		
		char ch = 'a';
		System.out.println(ch + 3);
		
		int i = 'a' + 3;
		System.out.println(i);
		
	}
}
